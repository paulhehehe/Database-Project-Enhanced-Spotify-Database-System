  CREATE OR REPLACE FORCE EDITIONABLE VIEW "GROUPUSER"."PLAYLIST_VIEW" ("ID", "PLAYLISTNAME", "CREATEDBYUSERID", "ISPUBLIC") AS 
  SELECT PLAYLIST.ID, PLAYLIST.PLAYLISTNAME, PLAYLIST.CREATEDBYUSERID, ISPUBLIC.ISPUBLIC
FROM PLAYLIST
JOIN PLAYLIST_ISPUBLIC ON PLAYLIST_ISPUBLIC.ID_PLAYLIST = PLAYLIST.ID
JOIN ISPUBLIC ON PLAYLIST_ISPUBLIC.ISPUBLIC_ID = ISPUBLIC.ID_ISPUBLIC
WHERE PLAYLIST_ISPUBLIC.END_DATE IS NULL
;
